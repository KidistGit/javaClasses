package com.class07;

public class WhileLoopPractice {

	public static void main(String[] args) {
		
// print only odd number from50 to 100
// 
		int a=50;
		while(a<=100) {
			System.out.println("a");
			if(a%2==0) {
				System.out.println(a);
			}
			a++;
			}
			System.out.println("*******");
			//print only even numbers from 100 to 1
			int i=1;
			while(i<=100) {
				System.out.println("i");
				if(i%2==0) {
					System.out.println(i);
				}
			i++;
			}
			System.out.println("*********");				
		
		}
		
	}

